public class Variables {
    public static void main(String[] args) {
        // primitive variables
        // whole numbers, negative or positive
        int num1 = 15;
        // numbers with decimals (with limit)
        double num2 = 85234.25;
        // true or false
        boolean javaIsNewToMe = true;
        // single letter or punctuation (use single quotes)
        char firstInitial = 'B';
        // Non-primitive variables
        // Strings are Objects (use double quotes)
        String name = "Britt";
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(javaIsNewToMe);
        System.out.println(firstInitial);
        System.out.println(name);
        System.out.println(name + "s first initial is " + firstInitial);
    }
}